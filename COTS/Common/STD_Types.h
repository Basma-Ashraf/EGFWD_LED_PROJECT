#define SET_BIT(REG,PIN)		                             REG |= ((U32)1<<PIN)
#define CLR_BIT(REG,PIN)		                             REG &= ~((U32)1<<PIN)
#define TOGGLE_BIT(REG,PIN)		                             REG ^= ((U32)1<<PIN)
#define GET_BIT(REG,PIN)		                            (REG >> PIN) & (U32)0x01

