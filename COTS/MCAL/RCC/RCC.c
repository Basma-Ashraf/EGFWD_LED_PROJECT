void RCC_voidEnablePeripheral(void)
{
	//enable clock to GPIO PORTA
	SET_BIT (RCC_GPIO,GPIO_PORT_F);
}

void RCC_voidIntialize (void)
{
	//choose main oscillator as system clock source 
	CLR_BIT(RCC_EN,0);
}


